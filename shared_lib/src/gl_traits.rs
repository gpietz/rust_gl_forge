use anyhow::Result;

//////////////////////////////////////////////////////////////////////////////
// - Bindable -
//////////////////////////////////////////////////////////////////////////////

pub trait Bindable {
    type Target;

    fn bind(&mut self) -> Result<&mut Self::Target>;
    fn unbind(&mut self) -> Result<&mut Self::Target>;
    fn is_bound(&self) -> bool;
}

//////////////////////////////////////////////////////////////////////////////
// - Deletable -
//////////////////////////////////////////////////////////////////////////////

pub trait Deletable {
    fn delete(&mut self) -> Result<()>;
}

//////////////////////////////////////////////////////////////////////////////
// - ToOpenGL -
//////////////////////////////////////////////////////////////////////////////

pub trait ToOpenGL {
    fn to_opengl(&self) -> u32;
}

//////////////////////////////////////////////////////////////////////////////
// - ToOpenGLenum -
//////////////////////////////////////////////////////////////////////////////

pub trait ToOpenGLenum {
    fn to_opengl_enum(&self) -> gl::types::GLenum;
}
